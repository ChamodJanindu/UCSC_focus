function startTimer() {
    if (timerInterval) return;
    const sub = document.getElementById('timer-subject').value;
    if (currentMode === 'study' && !sub) return alert('Please select a module to focus on.');

    document.getElementById('timer-status').textContent = currentMode === 'study' ? "Neural synthesis active..." : "Physiological recovery in progress...";
    
    startFallingLeaves();

    timerInterval = setInterval(() => {
        timerSeconds--;
        updateTimerDisplay();
        if (timerSeconds <= 0) {
            clearInterval(timerInterval);
            timerInterval = null;
            stopFallingLeaves();
            timerFinished();
        }
    }, 1000);
}

function pauseTimer() { 
    clearInterval(timerInterval); 
    timerInterval = null; 
    stopFallingLeaves();
    document.getElementById('timer-status').textContent = "Session interrupted.";
}

function resetTimer() {
    pauseTimer();
    stopFallingLeaves();
    const mins = parseInt(document.getElementById('timer-duration').value) || 25;
    timerSeconds = mins * 60;
    totalTimerSeconds = mins * 60;
    updateTimerDisplay();
}

function startFallingLeaves() {
    const container = document.getElementById('leaves-container');
    if (!container) return;
    
    stopFallingLeaves();
    
    const colors = ['#e67e22', '#d35400', '#f39c12', '#c0392b', '#b8860b'];
    
    // Original high-density frequency (200ms)
    leafInterval = setInterval(() => {
        const leaf = document.createElement('div');
        leaf.className = 'leaf';
        
        const size = Math.random() * 18 + 10;
        const duration = Math.random() * 4 + 4;
        const left = Math.random() * 100;
        const color = colors[Math.floor(Math.random() * colors.length)];
        
        // Random drift and rotation using CSS variables
        const drift = (Math.random() * 300 - 150) + 'px';
        const rev = (Math.random() * 1000 + 360) + 'deg';
        
        leaf.style.setProperty('--drift', drift);
        leaf.style.setProperty('--rev', rev);
        
        leaf.style.left = left + 'vw';
        leaf.style.width = size + 'px';
        leaf.style.height = (size * 0.6) + 'px';
        leaf.style.backgroundColor = color;
        leaf.style.animationDuration = duration + 's';
        
        container.appendChild(leaf);
        
        setTimeout(() => {
            if (leaf.parentNode === container) {
                leaf.remove();
            }
        }, duration * 1000);
    }, 200);
}

